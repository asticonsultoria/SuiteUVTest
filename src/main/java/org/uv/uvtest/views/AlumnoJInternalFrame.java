    private void btnTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerminarActionPerformed
        alumno.setCalificacion(resultadoFinal);
        alumno.setNormal(resultadoNormal);
        alumno.setMotivacion(resultadoMotivacion);
        alumno.setPresicion(resultadoPresicion);
        boolean result = dao.update(alumno);
        if(result){
            int r =  JOptionPane.showConfirmDialog(null, "El examen ha terminado con exito. \n Desea terminar el examen?", "Atención",
			JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if(r == JOptionPane.YES_OPTION){
                dispose();
                mdi.remove(this);
            }
        }else{
            Inicio.showErrorMessage("La información del Alumno no fue guardada, contacte al proveedor.");
        }
    }//GEN-LAST:event_btnTerminarActionPerformed

