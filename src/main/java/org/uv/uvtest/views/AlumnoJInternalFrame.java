    private void checkLevel(int level){
        validLabel.setText("");
        switch(level){
            case 1:
                if(m1.isSelected() && l1.isSelected()){
                    moreGroup.clearSelection();
                    lessGroup.clearSelection();
                    validLabel.setText("No es posible elegir M y L del mismo nivel");
                }
                break;
            case 2:
                if(m2.isSelected() && l2.isSelected()){
                    moreGroup.clearSelection();
                    lessGroup.clearSelection();
                    validLabel.setText("No es posible elegir M y L del mismo nivel");
                }
                break;
            case 3:
                if(m3.isSelected() && l3.isSelected()){
                    moreGroup.clearSelection();
                    lessGroup.clearSelection();
                    validLabel.setText("No es posible elegir M y L del mismo nivel");
                }
                break;
            case 4:
                if(m4.isSelected() && l4.isSelected()){
                    moreGroup.clearSelection();
                    lessGroup.clearSelection();
                    validLabel.setText("No es posible elegir M y L del mismo nivel");
                }
                break;
        }
    }
    
